package veritipi1;
 
public class Veritipi1 {
 
    public static void main(String[] args) {
 
        byte byteValue = 4;
        short shortValue = 7;
        int integerValue = 234;
        long longValue = 12332;
 
        float floatValue = 34.5f;
        double doubleValue = 345.2;
 
        char charValue1 = 65;
        char charValue2 = 'A';
 
        boolean rightValue = true;
        boolean wrongValue = false;
 
        System.out.println("Byte Value: " + byteValue);
        System.out.println("Short Value: " + shortValue);
        System.out.println("Integer Value: " + integerValue);
        System.out.println("Long Value: " + longValue);
 
        System.out.println("Float Value: " + floatValue);
        System.out.println("Double Value: " + doubleValue);
 
        System.out.println("Char Value 1: " + charValue1);
        System.out.println("Char Value 2: " + charValue2);
 
        System.out.println("Boolean Right: " + rightValue);
        System.out.println("Boolean Wrong: " + wrongValue);
    }
}